package operators;

public class ComparisionOperators {

	public static void main(String[] args) {
		//3. Conditional/ Comparison Operators ==> to compare primitive data types (> , <, >=, <=, ==, != ) (Result : true/false)
		
		int x = 10;
		int y = 20;
		
		System.out.println(x==y);
		
		System.out.println(x!=y);
		
		System.out.println(x>y);
		
		System.out.println(x<y);
		
		System.out.println(x>=y);
		
		System.out.println(x<=y);
		

	}

}
