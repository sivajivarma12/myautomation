package operators;

public class AssignmentOperators {

	public static void main(String[] args) {
	
		//2. Assignment Operators ==> to assign value to variables (= ,+=, -=, *=, /=, %=)	
		
		int x = 20;
		System.out.println(x);
		
		x+=5;//x=x+5;
		System.out.println(x);
		
		x-=15;//x=x-15
		System.out.println(x);
		
		x*=5;
		System.out.println(x);
		
		x/=10;
		System.out.println(x);
		
		x%=5;
		System.out.println(x);

	}

}
